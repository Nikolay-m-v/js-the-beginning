// Напишете функция, която получава положително число n, въведено от потребителя и печата числата от n до 1 в обратен ред.
// Въведеното число n, винаги ще бъде по-голямо от 1.

function numbersNto1(number) {
  let n = number;
  for (i = n; i > 0; i--) {
    console.log(i);
  }
}

numbersNto1(2);
